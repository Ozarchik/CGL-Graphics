extern const std::vector<cgl::Vertex> vertices;

cgl::Cube::Cube()
    : Mesh(vertices)
{
    m_primitiveData.drawType = cgl::DrawType::Elements;
    m_primitiveData.type = cgl::Primitive::Triangle;
    m_primitiveData.size = 36;
    m_primitiveData.offset = 0;
}

cgl::Cube::~Cube()
{
}

void cgl::Cube::draw(Shader &shader)
{
    shader.use();
    m_buffer.bind();
    RenderContext::instance().render(m_primitiveData);
    m_buffer.unbind();
    shader.done();
}


